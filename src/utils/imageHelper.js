export const returnIcon = (type) => {
  switch(type) {
    case "Motorcycle":
      return require('../assets/vehicles/motor_nobg.png');
    case "Truck":
      return require('../assets/vehicles/exclusive_nobg.png');
    case "Ship":
      return require('../assets/vehicles/ship_nobg.png');
    default:
      return require('../assets/vehicles/motor_nobg.png');
  }
}

